# Задаём первоначальное большое значение для дальнейшего сравнения с минимумом
ans = 1000000000

for x in range(12):
    for y in range(12):
        # Вычисляем значение арифм выражения
        t = int(str(y) + 'AA' + str(x), 12) + int(str(x) + '02' + str(y), 14)
        if t % 80 == 0:
            # Если полученное значение меньше, то меняем ответ, если нет - оставляем
            ans = min(ans, t)

print(ans // 80)
